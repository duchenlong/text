#include<stdio.h>
#include<math.h>


int main()
{
	int num = 0;
	int count = 0;
	int i = 0;
	scanf("%d", &num);
	int i1 = 0, i2 = 0, i3 = 0;
	int x1 = sqrt(num / 4) + 1;
	int x2 = sqrt(num / 3) + 1;
	int x3 = sqrt(num / 2) + 1;
	for (i1 = 0; i1 <= x1; i1++)
	{
		for (i2 = i1; i2 <= x2; i2++)
		{
			for (i3 = i2; i3 <= x3; i3++)
			{
				int x4 = num - i1*i1 - i2*i2 - i3*i3;
				if (x4 == (int)sqrt(x4)*(int)sqrt(x4) && (int)sqrt(x4)>=x3)
				{
					printf("%d %d %d %d\n", i1, i2, i3, (int)sqrt(x4));
					exit();
				}
			}
		}
	}
	return 0;
}